// 引入 request 文件
import request from '../request'

// 获取卡
export const cardList = (params) => {
    return request({
        url: "/card/list",
        method: 'post',
        data: params
    })
}
// 绑定电卡
export const bindCard = (params) => {
    return request({
        url: "/card/bindCard",
        method: 'post',
        data: params
    })
}